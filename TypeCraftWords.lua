TypeCraftWords.enabledDifficulties = {
    easy = true,
    medium = true,
    hard = false,
    hardcore = false,
    caseSensitive = false
}

TypeCraftWords.combinedWordList = {}

function TypeCraftWords.updateCombinedWordList()
    TypeCraftWords.combinedWordList = {}

    for poolName, isPoolEnabled in pairs(TypeCraftWords.enabledPools) do
        local wordPool = TypeCraftWords[poolName]

        if isPoolEnabled and wordPool then
            for _, word in ipairs(wordPool) do
                local len = #word

                -- Filter by difficulty
                if (TypeCraftWords.enabledDifficulties.easy and len <= 4) or
                   (TypeCraftWords.enabledDifficulties.medium and len > 4 and len <= 8) or
                   (TypeCraftWords.enabledDifficulties.hard and len > 8) then
                    table.insert(TypeCraftWords.combinedWordList, word)
                end
            end
        end
    end

    -- Use fallback if nothing matched
    if #TypeCraftWords.combinedWordList == 0 then
        TypeCraftWords.combinedWordList = TypeCraftWords.fallback or {
            "none", "empty", "null", "void", "nil", "nada", "zilch", "zero"
        }
    end
end
function TypeCraftWords.pickRandomWord()
    -- Ensure the combined list is up to date
    TypeCraftWords.updateCombinedWordList()

    local pool = TypeCraftWords.combinedWordList

    -- Fallback if nothing in the combined list
    if #pool == 0 then
        pool = TypeCraftWords.fallback or {
            "none", "empty", "null", "void", "nil", "nada", "zilch", "zero"
        }
    end

    -- Pick and return a random word
    local index = math.random(1, #pool)
    return pool[index]
end

